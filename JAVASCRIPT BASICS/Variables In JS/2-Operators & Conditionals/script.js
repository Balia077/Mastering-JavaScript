// OPERATORS & CONDITIONALS

//1. Arithmetic Operators:

//i-binary operators [+, -, *, /, %]
//ii-unary operators [++, --]


//1.Addition

let a = 20;
let b = 10;

console.log(a + b); //30

//2.Subtraction

console.log(a - b); //10

//3.Multiplication

console.log(a * b); //200

//4.Division

console.log(a / b); //2

//5.Modulus

console.log(a % b); //0

//6.Increment

console.log(a++); //20
console.log(a); //21

//7.Decrement

console.log(b--); //10
console.log(b); //9

console.log(a**b); //794280046581

//Unary Operators
//1. Pre-increment[First increment then use]
let x = 10;
console.log(++x);   //11
console.log(x);     //11

//2. Post-increment[First use then increment]
let y = 10;
console.log(y++);   //10
console.log(y);     //11

//3. Pre-decrement[First decrement then use]
let z = 10;
console.log(--z);   //9
console.log(z);     //9

//4. Post-decrement[First use then decrement]
let w = 10;
console.log(w--);   //10
console.log(w);     //9 

